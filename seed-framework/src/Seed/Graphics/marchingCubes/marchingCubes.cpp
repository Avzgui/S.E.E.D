unsigned char MarchingCubes::computeCube(float d1, float d2, float d3, float d4, float d5, float d6, float d7, float d8)
{
	unsigned char corner = 0x00;
	if (d1 <= 0)
	{
		corner = 1;
	}
	if (d2 <= 0)
	{
		corner += 2;
	}
	if (d3 <= 0)
	{
		corner += 4;
	}
	if (d4 <= 0)
	{
		corner += 8;
	}
	if (d5 <= 0)
	{
		corner += 16;
	}
	if (d6 <= 0)
	{
		corner += 32;
	}
	if (d7 <= 0)
	{
		corner += 64;
	}
	if (d8 <= 0)
	{
		corner += 128;
	}
	return corner;
}

